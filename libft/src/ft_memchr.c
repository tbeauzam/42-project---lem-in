#include "libft.h"

void	*ft_memchr(void const *s, int c, size_t n)
{
	size_t	i;
	char	*temp;

	i = 0;
	temp = (char *)s;
	while (i < n)
	{
		if ((unsigned char)temp[i] == (unsigned char)c)
		{
			temp += i;
			return ((void *)temp);
		}
		i += 1;
	}
	return (NULL);
}
