#include "libft.h"

int		ft_atoi(char const *str)
{
	int		nb;
	int		neg;
	int		i;

	nb = 0;
	neg = -1;
	i = 0;
	while (str[i] > 0 && str[i] <= 32)
		i += 1;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			neg *= -1;
		i += 1;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb *= 10;
		nb -= (str[i] - '0');
		i += 1;
	}
	nb *= neg;
	return (nb);
}
