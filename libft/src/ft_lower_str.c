#include "libft.h"

char		*ft_lower_str(char *s)
{
	int		i;

	i = 0;
	while (s[i])
	{
		if (ft_isupper(s[i]))
			s[i] = ft_tolower(s[i]);
		i += 1;
	}
	return (s);
}
