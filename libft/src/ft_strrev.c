#include "libft.h"

char	*ft_strrev(char *s)
{
	int		i;
	int		j;
	int		k;
	char	c;

	i = 0;
	j = ft_strlen(s) - 1;
	k = 0;
	while (i < (j / 2) + 1)
	{
		c = s[i];
		s[i] = s[j - k];
		s[j - k] = c;
		i += 1;
		k += 1;
	}
	return (s);
}
